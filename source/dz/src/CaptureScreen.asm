bmp_event PROC USES esi edi ebx

	call	old_event
	.if	eax == MOUSECMD
		mousex()
		mov	edi,eax
		mousey()
		mov	esi,eax
		mov	ebx,tdialog
		movzx	edx,[ebx].S_DOBJ.dl_rect.rc_x
		movzx	ecx,[ebx].S_DOBJ.dl_rect.rc_y
		add	edx,21
		add	ecx,4
		scputf( edx, ecx, 0, 0, "%d\n%d", edi, esi )
		sub	edx,21
		sub	ecx,4
		mov	eax,MOUSECMD
		.if	edi < edx || esi < ecx
			xor	eax,eax
		.else
			add	dl,[ebx].S_DOBJ.dl_rect.rc_col
			add	cl,[ebx].S_DOBJ.dl_rect.rc_row
			dec	ecx
			.if	edx < edi || ecx < esi
				xor	eax,eax
			.endif
		.endif
	.endif
	ret
bmp_event ENDP

read_screen PROC USES esi edi ebx
	.if	rsopen( IDD_SaveScreen )
		mov	ebx,eax
		mov	edi,[ebx].S_DOBJ.dl_object
		mov	[edi].S_TOBJ.to_data,offset bmpfile
		strcpy( [edi+16].S_TOBJ.to_data, "0" )
		strcpy( [edi+32].S_TOBJ.to_data, "0" )
		sprintf( [edi+48].S_TOBJ.to_data, "%d", _scrcol )
		mov	eax,_scrrow
		inc	eax
		sprintf( [edi+64].S_TOBJ.to_data, "%d", eax )
		dlinit( ebx )
		mov	eax,tgetevent
		mov	tgetevent,bmp_event
		mov	old_event,eax
		.if	rsevent( IDD_SaveScreen, ebx )
			atol ( [edi+16].S_TOBJ.to_data )
			mov	rect.rc_x,al
			atol ( [edi+32].S_TOBJ.to_data )
			mov	rect.rc_y,al
			atol ( [edi+48].S_TOBJ.to_data )
			mov	scrcol,eax
			mov	rect.rc_col,al
			atol ( [edi+64].S_TOBJ.to_data )
			mov	rect.rc_row,al
			.if	eax
				dec	eax
			.endif
			mov	scrrow,eax
			mov	eax,1
		.endif
		mov	ecx,old_event
		mov	tgetevent,ecx
		dlclose( ebx )
		mov	eax,edx
	.endif
	.if	eax
		mov	eax,scrrow
		inc	eax
		mul	scrcol
		shl	eax,1
		.if	malloc( eax )
			mov	screen,eax
			rcread( rect, eax )
			mov	eax,1
		.endif
	.endif
	ret
read_screen ENDP

font_putc PROC USES esi edi ecx
	;
	; EAX = Screen word
	; ESI = Font line [15..0]
	;
	movzx	ebx,al	; mask = *(font + (AL * 16) + SI);
	shl	ebx,4
	movzx	esi,font8x16[ebx+esi]
	mov	edi,4	; output 4 byte
	movzx	eax,ah
    fontputc_00:
	mov	ecx,eax
	test	esi,128
	jnz	fontputc_01
	shr	cl,4
	jmp	fontputc_02
    fontputc_01:
	and	cl,15
    fontputc_02:
	shl	ecx,4
	shl	esi,1
	push	eax
	test	esi,128
	jnz	fontputc_03
	shr	al,4
	jmp	fontputc_04
    fontputc_03:
	and	al,15
    fontputc_04:
	or	al,cl
	shl	esi,1
	call	oputc
	pop	eax
	jz	fontputc_05
	dec	edi
	jnz	fontputc_00
	xor	eax,eax
	inc	eax
	jmp	fontputc_06
    fontputc_05:
	xor	eax,eax
    fontputc_06:
	ret
font_putc ENDP

font_putline PROC USES esi edi ; EAX = line [24..0]
	movzx	ebx,al
	mov	eax,scrcol
	add	eax,eax
	mul	ebx
	add	eax,screen
	mov	edi,eax
	mov	esi,15
    fontputl_00:
	mov	ecx,scrcol
	push	edi
    fontputl_01:
	movzx	eax,WORD PTR [edi]
	add	edi,2
	call	font_putc
	jz	fontputl_05
	dec	ecx
	jnz	fontputl_01
	pop	edi
	dec	esi
	cmp	esi,-1
	jne	fontputl_00
    fontputl_04:
	xor	eax,eax
	ret
    fontputl_05:
	pop	edi
	jmp	fontputl_04
font_putline ENDP

cmsavebmp PROC USES esi edi ebx
	mov	eax,scrrow
	inc	eax
	shl	ax,4
	mov	brow,eax
	mov	ebx,eax
	mov	eax,scrcol
	shl	ax,3
	mov	bcol,eax
	shr	ebx,1
	mul	ebx
	mov	bmz,eax
	add	eax,SIZE S_BMP
	mov	bfz,eax
	.if	SDWORD PTR ioopen( addr STDO, addr bmpfile, M_WRONLY, 10000h ) > 0
		.if	iowrite( addr STDO, addr Bitmap, SIZE S_BMP )
			mov	esi,scrrow	; line [24..0]
			.repeat
				mov	eax,esi
				call	font_putline
				dec	esi
			.until	esi == -1
			ioflush( addr STDO )	; flush STDO
		.endif
		ioclose( addr STDO )
	.else
		eropen( addr bmpfile )
	.endif
	ret
cmsavebmp ENDP

MakeBMP PROC
	.if	read_screen()
		.if	bmpfile
			call	cmsavebmp
			stdmsg( "Screen Saved", "Screen saved to:\n%s", addr bmpfile )
		.endif
		free( screen )
	.endif
	ret
MakeBMP ENDP

CSIdle PROC
	mov	eax,keyshift
	mov	eax,[eax]
	and	eax,POPUPKEY
	.if	eax == POPUPKEY
		.if	!active
			mov  active,1
			call MakeBMP
		.endif
	.else
		mov  active,0
		call idleh
	.endif
	ret
CSIdle ENDP

