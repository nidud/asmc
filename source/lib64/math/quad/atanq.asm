atanq proc vectorcall Q:XQFLOAT

  local x:__m128i
  local k:SINT, sign:SINT

    movaps  x,xmm0
    mov     eax,x.m128i_u32[12]
    xor     ecx,ecx
    shld    ecx,eax,1
    and     eax,0x7fffffff
    mov     k,eax
    mov     sign,ecx

    .repeat

        ; Check for IEEE special cases.

        .if eax >= 0x7fff0000
            ;
            ; NaN.
            ;
            and eax,0xffff
            or  eax,x.m128i_u32[0]
            or  eax,x.m128i_u32[4]
            or  eax,x.m128i_u32[8]
            .if eax

                addq(xmm0, xmm0)
            .else
                ;
                ; Infinity.
                ;
                lea     rax,atan_table
                mov     rdx,[rax+83*16+8]
                rol     rcx,1
                or      rdx,rcx
                movq    xmm0,[rax+83*16]
                movq    xmm1,rdx
                shufpd  xmm0,xmm1,0
            .endif
            .break
        .endif

        .if eax <= 0x3fc50000 ; |x| < 2**-58
            ;
            ; Raise inexact.
            ;
            movaps  xmm1,xmm0
            movhlps xmm0,addq(1.0e4930, xmm1)
            movq    rax,xmm0
            movaps  xmm0,x
            rol     rax,1
            .break .if eax & 1

            mov     eax,k
            mov     ecx,sign
        .endif

        .if eax >= 0x40720000 ; |x| > 2**115
            ;
            ; Saturate result to {-,+}pi/2
            ;
            lea     rax,atan_table
            mov     rdx,[rax+83*16+8]
            rol     rcx,1
            or      rdx,rcx
            movq    xmm0,[rax+83*16]
            movq    xmm1,rdx
            shufpd  xmm0,xmm1,0
            .break
        .endif

        .if ecx

            xor x.m128i_u8[15],0x80
        .endif

        .if eax >= 0x40024800 ; 10.25

            mov k,83
            movaps xmm5,divq(FLTQ(-1.0), x)
        .else
            ;
            ; Index of nearest table element.
            ; Roundoff to integer is asymmetrical to avoid cancellation when t < 0
            ; (cf. fdlibm).
            ;
            mov k,cvtq_i32(addq(mulq(FLTQ(8.0), x), FLTQ(0.25)))
            movaps xmm3,mulq(cvti32_q(k), FLTQ(0.125))

            ; Small arctan argument.
            movaps xmm2,addq(mulq(x, xmm3), FLTQ(1.0))
            movaps xmm5,divq(subq(x, xmm3), xmm2)
        .endif

        movq    rax,xmm0
        movhlps xmm0,xmm0
        movq    rdx,xmm0
        or      rax,rdx
        .ifnz
            ;
            ; Arctan of small argument t.
            ;
            movaps xmm4,mulq(xmm5, xmm5)

            addq(mulq(p4,   xmm4), p3)
            addq(mulq(xmm0, xmm4), p2)
            addq(mulq(xmm0, xmm4), p1)
            addq(mulq(xmm0, xmm4), p0)
            movaps xmm2,xmm0

            addq(mulq(addq(q4, xmm4), xmm4), q3)
            addq(mulq(xmm0, xmm4), q2)
            addq(mulq(xmm0, xmm4), q1)
            addq(mulq(xmm0, xmm4), q0)

            movaps xmm3,xmm0
            addq(divq(mulq(mulq(xmm5, xmm4), xmm2), xmm3), xmm5)
        .endif

        ; arctan x = arctan u + arctan t

        mov eax,k
        shl eax,4
        lea rdx,atan_table
        addq(xmm0, [rdx+rax])
        .if sign
            pxor xmm0,FLTQ(-0.0)
        .endif

    .until 1
    ret

atanq endp

