get_char proc

    .if ( eax > 127 )

        .for ( rdx = &_w, ecx = 0 : ecx < lengthof(_w) : ecx++ )

            .if ( ax == [rdx+rcx*2] )
                .return( ecx )
            .endif
        .endf
    .endif
    ret

get_char endp

put_char proc uses rsi rdi rbx
    ;
    ; EAX = Screen word
    ; ESI = Font line [15..0]
    ;
    movzx   ebx,al  ; mask = *(font + (AL * 16) + SI);
    shl     ebx,4
    lea     rcx,font8x16
    lea     rdx,[rbx+rsi]
    movzx   esi,byte ptr [rcx+rdx]
    mov     edi,4   ; output 4 byte
    movzx   eax,ah

    .while 1

        mov ecx,eax
        .if esi & 128
            and cl,15
        .else
            shr cl,4
        .endif
        shl ecx,4
        shl esi,1
        mov ebx,eax

        .if esi & 128
            and al,15
        .else
            shr al,4
        .endif
        or  al,cl
        shl esi,1
        fputc(eax, fp)

        .if eax == -1

            xor eax,eax
           .break
        .endif

        mov eax,ebx
        dec edi
        .ifz
            xor eax,eax
            inc eax
           .break
        .endif
    .endw
    ret

put_char endp

put_line proc uses rsi rdi rbx ; EAX = line [n..0]

   .new x:int_t

    movzx   ebx,al
    mov     eax,scrcol
    shl     eax,2
    mul     ebx
    add     rax,screen
    mov     rbx,rax
    mov     esi,15

    .while 1

        mov x,scrcol
        mov rdi,rbx
        .repeat
            movzx eax,word ptr [rdi]
            get_char()
            mov ah,[rdi+2]
            add rdi,4
            .if !put_char()
                xor eax,eax
               .break
            .endif
            dec x
        .untilz
        dec esi
        .if esi == -1
            xor eax,eax
           .break
        .endif
    .endw
    ret

put_line endp

savebmp proc uses rsi rdi rbx file:LPSTR

    mov eax,scrrow
    inc eax
    shl ax,4
    mov brow,eax
    mov ebx,eax
    mov eax,scrcol
    shl ax,3
    mov bcol,eax
    shr ebx,1
    mul ebx
    mov bmz,eax
    add eax,S_BMP
    mov bfz,eax

    mov fp,fopen(file, "wb")
    .if ( fp != NULL )
        .if fwrite(&Bitmap, S_BMP, 1, fp)

            mov esi,scrrow  ; line [n..0]
            .repeat
                mov eax,esi
                put_line()
                dec esi
            .until esi == -1
        .endif
        fclose(fp)
    .else
        perror(file)
    .endif
    ret

savebmp endp

_tmain proc argc:int_t, argv:array_t

    .new idd_file:string_t = 0
    .new src_idd:ptr
    .new bmp_file[_MAX_PATH]:char_t = 0

    .for ( rbx = argv, edi = 1 : edi < argc : edi++ )

        mov rcx,[rbx+rdi*LPSTR]
        mov eax,[rcx]
        .if ( al == '-' || al == '/' )

            lea rdx,Win10ColorTable
            mov ecx,16
            .if ( ah == 'u' )
                lea rsi,UbuntuColorTable
                xchg rdx,rdi
                rep movsd
                mov rdi,rdx
            .elseif ( ah == 'v' )
                lea rsi,VGAColorTable
                xchg rdx,rdi
                rep movsd
                mov rdi,rdx
            .endif

        .elseif ( idd_file == 0 )

            mov idd_file,rcx
        .else
            strcpy(&bmp_file, rcx)
        .endif
    .endf

    .if ( idd_file == 0 )

        _tprintf(
            "Usage: IDD2BMP [/options] <idd_file> [<out_file>]\n"
            "\n"
            "-u   use Ubuntu color\n"
            "-v   use VGA color\n"
            "-w   use Windows 10 color (default)\n"
            "\n" )
        .return(0)
    .endif

    .if ( bmp_file == 0 )

        .if strchr(strcpy(&bmp_file, idd_file), '.')

            strcpy(rax, ".bmp")
        .else
            strcat(&bmp_file, ".bmp")
        .endif
    .endif

    .if _access(idd_file, 0)

        perror(idd_file)
        exit(1)
    .endif

    .new h:int_t = _open(idd_file, O_RDONLY or O_BINARY, 0)
    .if ( h == -1 )

        perror(idd_file)
        exit(1)
    .endif

   .new size:long_t = _filelength(h)
    mov src_idd,malloc(size)
    _read(h, src_idd, size)
    _close(h)

   .new count:int_t

    mov     rbx,src_idd
    mov     rect,[rbx].ROBJ.rc
    movzx   eax,rect.col
    mov     scrcol,eax
    mul     rect.row
    mov     count,eax
    shl     eax,2
    mov     size,eax
    movzx   eax,rect.row
    dec     eax
    mov     scrrow,eax
    mov     screen,malloc(size)
    mov     rcx,src_idd
    add     rcx,ROBJ

    _rcunzip(rect, screen, rcx)

    savebmp(&bmp_file)
    free(screen)
    free(src_idd)
   .return(0)

