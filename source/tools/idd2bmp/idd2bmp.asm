Bitmap          db 'BM'
bfz             dd 1F400h + sizeof(S_BMP)  ; file size
                dd 0                    ;
                dd sizeof(S_BMP)           ; offset bit
                dd 40                   ;
bcol            dd 80*8
brow            dd 25*16
                dw 1                ; planes
                dw 4                ; bit count
                dd 0                ; compression
bmz             dd 1F400h           ; size image
                dd 2835             ; x pels per meter
                dd 2835             ; y pels per meter
                dd 16               ; color used
                dd 0                ; color important

